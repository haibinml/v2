#配合一键转换vmess工具使用
#在mode=后填   "固定"   或   "跟随" （不包括双引号）
#例(mode=跟随 或 mode=固定)
#填"跟随"则混淆为导入的节点的混淆
#填"固定"则混淆固定为下文填写的混淆
#不填则默认"跟随"，不按规矩填也默认"跟随"
###############


mode=跟随


###############


host=


###############
#↑↑填写固定的混淆↑↑

#执行即可！！























































































































































































































































































































































































































































































































































































































































































































































































































zt=`sed -n 10p ./#混淆设置.sh`
hx=`sed -n 16p ./#混淆设置.sh`
host=`echo ${hx##*=}`




if [[ "mode=" == ${zt} ]]||[[ "mode=跟随" == ${zt} ]]; then
echo "混淆模式=跟随节点"


elif [[ "mode=固定" == ${zt} ]]; then
echo -e "混淆模式=固定\n固定的混淆为"$host""
sed -i "/host=/chost=$host" /data/v2/v2节点/*.ini
sed -i "/v2host=/cv2host=$host" /data/v2/一键转换vmess并使用.sh




else
echo -e "提示:填写不合规\n混淆模式=跟随节点"

fi

rm -f ./*.bak